export function getDibcacStandard(controlId, objectiveId) {
  const m = controlId.match(/\d+\.\d+\.\d+$/)
  if (!m) return null
  const nist = m[0]
  // Try bracketed key first (e.g. "3.1.1[a]"); fall back to bare key (e.g. "3.4.4")
  // for source rows that have no per-objective breakdown (single-objective controls).
  return dibcacStandardsByObjective[`${nist}[${objectiveId}]`] ?? dibcacStandardsByObjective[nist] ?? null
}
