// =========================================================================
// Controlled Evidence Tag Registry
// =========================================================================
//
// A controlled vocabulary describing the KIND of evidence an artifact is.
// Used (in a later phase) to tag artifacts and to match artifacts to the
// evidence an objective expects. This file is static data only — no UI, no
// objective mappings, and no recommendation logic depend on it yet.
//
// A tag answers: "What kind of evidence is this?"
// A tag does NOT answer: "What objective does this support?"
// A tag is NOT: a file format, a control family, an objective number, a
// vendor/tool, a UI source, or an artifact's title.
//
// -------------------------------------------------------------------------
// GOVERNANCE RULE — when a new tag is justified
// -------------------------------------------------------------------------
// A new evidence tag is justified only when ALL of the following are true:
//   1. An assessor would request it by a different evidence name and review it
//      as a separate evidence object.
//   2. It represents a distinct kind of evidentiary fact, not merely a
//      different tool, vendor, file format, scope, or parameter.
//   3. It can plausibly be reused across more than one objective.
//
// Never create a new tag solely because:
//   - it satisfies a different objective
//   - it comes from a different tool
//   - it has a different file format
//   - it differs only by a threshold or setting value
//
// `producedBy` and `aliases` exist precisely so that tool/vendor/name
// variation does NOT spawn new tags.
// =========================================================================

